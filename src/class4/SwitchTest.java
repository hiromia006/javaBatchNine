package class4;

public class SwitchTest {
    public static void main(String[] args) {
        int mark = 700;

        switch (mark) {
            case 80:
                System.out.println("A++");
                break;
            case 70:
                System.out.println("B++");
                break;
            case 60:
                System.out.println("C++");
                break;
            default:
                System.out.println("All Pass");

        }
    }
}
