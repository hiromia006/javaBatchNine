package class2;

public class ArithmeticOperatorTest {

    public static void main(String[] args) {
        int number1=66, number2=99, number3;

        number3=number2-number1; //99-66=33
        System.out.println(number3);//33

        number3=number2+number3; // 99+33=132
        System.out.println(number3); //132

        number3=number2/number1;
        System.out.println(number3);//1

        number3=number2*number3; //99*1
        System.out.println(number3);//132*

        number3=number2%8; //3
        System.out.println(number3);//3
    }
}
